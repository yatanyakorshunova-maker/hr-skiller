def normalize_city(city: str) -> str:
    """Приводит название города к стандартному виду для сравнения."""
    if not city:
        return ""
    
    text = str(city).strip().lower()
    # Убираем префиксы
    text = re.sub(r'^(г\.|город|г |пос\.|поселок|посёлок|с\.|село|д\.|деревня|пгт\.|ст\.)\s*', '', text)
    # Оставляем только буквы, пробелы и дефис
    text = re.sub(r'[^а-яё\s-]', '', text)
    text = re.sub(r'\s+', ' ', text).strip()
    return text


def is_valid_russian_city(city: str) -> bool:
    """
    Проверяет, является ли строка названием российского города или крупного населённого пункта.
    Поддерживает частичные и нестрогие совпадения.
    """
    if not city:
        return False
    
    normalized = normalize_city(city)
    if not normalized or len(normalized) < 2:
        return False

    # Точное совпадение
    if normalized in RUSSIAN_CITIES:
        return True

    # Частичное совпадение (очень полезно для резюме)
    for known in RUSSIAN_CITIES:
        if known in normalized or normalized in known:
            return True

    return False


def get_valid_city(city: str) -> Optional[str]:
    """Возвращает нормализованное название города, если оно валидно, иначе None."""
    if is_valid_russian_city(city):
        return normalize_city(city).title()
    return None